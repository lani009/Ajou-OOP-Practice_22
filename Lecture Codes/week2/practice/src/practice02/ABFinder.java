package practice02;

public class ABFinder {
    public static void main(String[] args) {
        int[][] abArray = new int[100][2]; // A와 B의 조합을 담는 배열
        int cnt = 0; // 조합의 개수

        // 이 부분을 채워주세요.

        for (int i = 0; i < cnt; i++) {
            System.out.printf("%d %d\n", abArray[i][0], abArray[i][1]);
        }
    }
}
